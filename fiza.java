public class fiza {
  
     public static void main(String[] args) {
             int d = 20;
             int e = 30;
             int f = 35;
             int g = 35;
             System.out.println("d + e = " + (d + e) );
             System.out.println("d - e = " + (d - e) );
             System.out.println("d * e = " + (d * e) );
             System.out.println("e / d = " + (e / d) );
             System.out.println("e % d = " + (e % d) );
             System.out.println("f % d = " + (f % d) );
             System.out.println("d++   = " + (d++) );
             System.out.println("d--   = " + (d--) );
             // check the different in g++ and ++g
             System.out.println("g++   = " + (g++) );
             System.out.println("++g   = " + (++g) );
           }
        }   
